/**
  * @brief This function handles TIM5 global interrupt.
  */
void TIM5_IRQHandler(void)
{
  /* USER CODE BEGIN TIM5_IRQn 0 */
  
  switch (DISTANCE)
  {
    case MODE_0 : PWM_Disable(&htim4);
               break;
    case MODE_1 : PWM_Config(&htim4, TIM_CHANNEL_4, IMPULSE_1, COUNTER_6);
                  PWM_Enable(&htim4);
               break;
    case MODE_2 : PWM_Config(&htim4, TIM_CHANNEL_4, IMPULSE_1, COUNTER_5);
                  PWM_Enable(&htim4);
               break;
    case MODE_3 : PWM_Config(&htim4, TIM_CHANNEL_4, IMPULSE_1, COUNTER_4);
                  PWM_Enable(&htim4);
               break;
  }
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  
  if (slave) {
    HAL_TIM_Base_Stop_IT(&htim5);
    slave = false;
  }
  
  if (!slave && !master)
    TIM_Config(&htim12, (1 * 1000) - 1);
    
  
  /* USER CODE END TIM5_IRQn 0 */
  HAL_TIM_IRQHandler(&htim5);
  /* USER CODE BEGIN TIM5_IRQn 1 */

  /* USER CODE END TIM5_IRQn 1 */
}

